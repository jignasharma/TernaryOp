public class Alphabet_City
{
    public static void main(String []args)
    {
        char ch = 'A';
        switch (ch) {
            case 'A':
                System.out.println("Ahmedabad");//Print cities names
                break;
            case 'B':
                System.out.println("Bhavnagar");
                break;
            case 'C':
                System.out.println("Chanod");
                break;
            case 'D':
                System.out.println("Dakor");
                break;
            case 'E':
                System.out.println("Eklingji");
                break;
            case 'F':
                System.out.println("Fatepura");
                break;
            default:
                System.out.println("Invalid entry");
                break;

        }
    }
}
